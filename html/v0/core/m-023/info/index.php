                systems. Both LRU and MRU are strategies that consider the recent usage history of pages to determine
                which
                one to replace when a page fault occurs.</span></p>
        <p class="c2"><span class="c0"></span></p>
        <h1 class="c14" id="h.2kll5rsilmwm"><span class="c7" style="font-weight: bold; font-size: 26px;">2. LRU (Least
                Recently Used) Page Replacement
                Algorithm:</span>
        </h1>
        <h2 class="c1" id="h.tfmhxbp1sh9f"><span class="c10" style="text-decoration: underline;">Working:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Maintain a record of the order in which pages are accessed.</span></p>
        <h2 class="c1" id="h.svdhjt4cd7xq"><span class="c10" style="text-decoration: underline;">When a page fault
                occurs:</span></h2>
        <p class="c13"><span class="c0">Replace the page that has not been used for the longest period.</span></p>
        <h2 class="c1" id="h.8z1p2juomzmm"><span class="c10" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Effective in capturing temporal locality.</span></p>
        <h2 class="c1" id="h.vzaat4ik9mc3"><span class="c10" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Requires a mechanism to track page access history, which can be
                resource-intensive.</span></p>
        <h1 class="c14" id="h.g1eq5nclwhkt"><span class="c7" style="font-weight: bold; font-size: 26px;">3. MRU (Most
                Recently Used) Page Replacement
                Algorithm:</span>
        </h1>
        <h2 class="c1" id="h.k9wqkn4v1y68"><span class="c10" style="text-decoration: underline;">Working:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Maintain a record of the order in which pages are accessed.</span></p>
        <h2 class="c1" id="h.gwp8tdvv1gu8"><span class="c10" style="text-decoration: underline;">When a page fault
                occurs:</span></h2>
        <p class="c13"><span class="c0">Replace the page that has been used most recently.</span></p>
        <h2 class="c1" id="h.e6j5v22qkfc7" style="font-weight: bold; font-size: 26px;"><span class="c10"
                style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Simplicity in implementation.</span></p>
        <p class="c13"><span class="c0">Suitable for environments where recently used pages are more likely to be used
                again.</span></p>
        <h2 class="c1" id="h.giuis85tbh1z"><span class="c10" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">May not perform optimally in scenarios with temporal locality.</span></p>
        <h1 class="c14" id="h.ucyj4mz8h8qd"><span class="c7" style="font-weight: bold; font-size: 26px;">4. Example
                Scenario:</span></h1>
        <p class="c13"><span class="c0">Consider a memory system with three frames (A, B, C) and a sequence of page
                requests: 1, 2, 3, 4, 1, 2, 5, 1, 2, 3.</span></p>
        <p class="c2"><span class="c0"></span></p>
        <h2 class="c1" id="h.am36udgpz0h2"><span class="c10" style="text-decoration: underline;">LRU:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Initial state: | - | - | - |</span></p>
        <p class="c13"><span class="c0">Page 1 is requested: | 1 | - | - |</span></p>
        <p class="c13"><span class="c0">Page 2 is requested: | 1 | 2 | - |</span></p>
        <p class="c13"><span class="c0">Page 3 is requested: | 1 | 2 | 3 |</span></p>
        <p class="c13"><span class="c0">Page 4 is requested: | 4 | 2 | 3 | (Page 1 is the least recently used, so it is
                replaced)</span></p>
        <p class="c13"><span class="c0">... and so on.</span></p>
        <h2 class="c1" id="h.lnkkogvbe4e1"><span class="c10" style="text-decoration: underline;">MRU:</span></h2>
        <p class="c2"><span class="c0"></span></p>
        <p class="c13"><span class="c0">Initial state: | - | - | - |</span></p>
        <p class="c13"><span class="c0">Page 1 is requested: | 1 | - | - |</span></p>
        <p class="c13"><span class="c0">Page 2 is requested: | 1 | 2 | - |</span></p>
        <p class="c13"><span class="c0">Page 3 is requested: | 1 | 2 | 3 |</span></p>
        <p class="c13"><span class="c0">Page 4 is requested: | 1 | 2 | 4 | (Page 3 is the most recently used, so it is
                replaced)</span></p>
        <p class="c13"><span class="c0">... and so on.</span></p>
        <h1 class="c14" id="h.jcy61ixq98ce"><span class="c7" style="font-weight: bold; font-size: 26px;">5.
                Conclusion:</span></h1>
        <p class="c13"><span class="c0">LRU and MRU are page replacement algorithms that focus on the recency of page
                usage.
                While LRU is effective in capturing temporal locality, it requires additional mechanisms for maintaining
                access history. MRU, on the other hand, is simpler but may not perform optimally in scenarios with
                temporal
                locality.</span></p>
