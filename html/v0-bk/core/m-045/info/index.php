        <h1 class="c17" id="h.8uuwopzejn4n"><span class="c14" style="font-weight: bold; font-size: 26px;">1. SCAN Disk
                Scheduling Algorithm:</span></h1>
        <h2 class="c6" id="h.rkqv5q1h6rms"><span class="c9" style="text-decoration: underline;">Working:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">The disk arm moves in one direction (e.g., from the outermost to the innermost
                track).</span></p>
        <p class="c0"><span class="c4">Services requests in the direction of movement.</span></p>
        <p class="c0"><span class="c4">When reaching the end, reverses direction.</span></p>
        <h2 class="c6" id="h.xy1pzf8g6gwm"><span class="c9" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">Efficient for systems with high request density.</span></p>
        <p class="c0"><span class="c4">Reduces the likelihood of starvation.</span></p>
        <h2 class="c6" id="h.ntxeq6x486p"><span class="c9" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">May lead to increased seek times for requests at the end tracks.</span></p>
        <h1 class="c17" id="h.7mwbks7jfsu7"><span class="c14" style="font-weight: bold; font-size: 26px;">2. C-SCAN Disk
                Scheduling Algorithm:</span></h1>
        <h2 class="c6" id="h.n4x4v1odhd2e"><span class="c9" style="text-decoration: underline;">Working:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">Similar to SCAN but only services requests in one direction.</span></p>
        <p class="c0"><span class="c4">When reaching the end, the arm &quot;wraps around&quot; to the beginning.</span>
        </p>
        <h2 class="c6" id="h.l9nv9erx8gei"><span class="c9" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">Reduces the impact of seeking from the last track to the first track.</span></p>
        <p class="c0"><span class="c4">Provides a more uniform wait time for requests.</span></p>
        <h2 class="c6" id="h.vkf7kui3604"><span class="c9" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">May still result in increased seek times for requests near the end tracks.</span>
        </p>
        <h1 class="c17" id="h.mxgqoh2ptbv9"><span class="c14" style="font-weight: bold; font-size: 26px;">3. C-LOOK Disk
                Scheduling Algorithm:</span></h1>
        <h2 class="c6" id="h.n38hybt30wk2"><span class="c9" style="text-decoration: underline;">Working:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">Similar to C-SCAN but does not service requests at the empty area during the
                return
                sweep.</span></p>
        <p class="c0"><span class="c4">The arm moves to the last request and then reverses direction.</span></p>
        <h2 class="c6" id="h.8tnbv4jfgmod"><span class="c9" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">More efficient in terms of seek time compared to C-SCAN.</span></p>
        <p class="c0"><span class="c4">Reduces the potential wait time for requests near the end tracks.</span></p>
        <h2 class="c6" id="h.mzl9dpheiuf6"><span class="c9" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c0 c3"><span class="c4"></span></p>
        <p class="c0"><span class="c4">May still lead to increased seek times for certain requests.</span></p>
