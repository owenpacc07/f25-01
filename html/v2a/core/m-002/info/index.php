        <p class="c9 title" id="h.xolczaxrdvja"><span class="c15"
                style="color: green; text-decoration: underline; font-weight: bold;">A Quick Abstract:</span></p>
        <h1 class="c4" id="h.lv92bk4hp3s"><span class="c12 c8" style="font-weight: bold; font-size: 26px;">1.
                Introduction to Non-Preemptive SJF:</span></h1>
        <h2 class="c10" id="h.vm2dfm2aotcd"><span class="c11 c8" style="text-decoration: underline;">Definition:</span>
        </h2>
        <p class="c0"><span class="c5">&nbsp;Non-Preemptive SJF is a CPU scheduling algorithm that selects the process
                with
                the shortest total burst time for execution, without preempting the currently running process.</span>
        </p>
        <p class="c0 c2"><span class="c5"></span></p>
        <h1 class="c4" id="h.qiiizv5xl1gt"><span class="c8 c12" style="font-weight: bold; font-size: 26px;">2. Burst
                Time and Arrival:</span></h1>
        <h2 class="c10" id="h.vnyjb54h92ud"><span class="c11 c8" style="text-decoration: underline;">Burst Time:</span>
        </h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Each process has an associated burst time, representing the total time it
                requires on
                the CPU.</span></p>
        <h2 class="c10" id="h.ke8pzaoie2sc"><span class="c11 c8" style="text-decoration: underline;">Arrival of
                Processes:</span></h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Processes are added to the ready queue upon arrival.</span></p>
        <h1 class="c4" id="h.8sptcacxl02w"><span class="c12 c8" style="font-weight: bold; font-size: 26px;">3. Execution
                and Process Selection:</span></h1>
        <h2 class="c10" id="h.kd614p3cxf58"><span class="c11 c8" style="text-decoration: underline;">Execution:</span>
        </h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">The process with the shortest burst time is selected for execution.</span></p>
        <p class="c0"><span class="c5">The selected process runs until completion without interruption.</span></p>
        <h2 class="c10" id="h.wgukdxphdd6l"><span class="c11 c8" style="text-decoration: underline;">Completion:</span>
        </h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Upon completion, the next process with the shortest burst time is chosen.</span>
        </p>
        <h1 class="c4" id="h.rpf8v2430drh"><span class="c12 c8" style="font-weight: bold; font-size: 26px;">4. Waiting
                Time and Turnaround Time:</span></h1>
        <h2 class="c10" id="h.e267g2dsfb6h"><span class="c11 c8" style="text-decoration: underline;">Waiting
                Time:</span></h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Waiting time is the cumulative time a process spends in the ready queue.</span>
        </p>
        <h2 class="c10" id="h.he9jm1nt40c"><span class="c11 c8" style="text-decoration: underline;">Turnaround
                Time:</span></h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Turnaround time is the overall time taken by a process from submission to
                completion.</span></p>
        <h1 class="c4" id="h.ptg23vj9y853"><span class="c12 c8" style="font-weight: bold; font-size: 26px;">5. Pros and
                Cons:</span></h1>
        <h2 class="c10" id="h.jp622ji6zoqf"><span class="c11 c8" style="text-decoration: underline;">Pros:</span></h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Optimizes turnaround time by prioritizing shorter processes.</span></p>
        <p class="c0"><span class="c5">Minimizes waiting time for processes with shorter burst times.</span></p>
        <h2 class="c10" id="h.n2tnhsdjsuli"><span class="c8 c11" style="text-decoration: underline;">Cons:</span></h2>
        <p class="c0 c2"><span class="c5"></span></p>
        <p class="c0"><span class="c5">Can lead to &quot;starvation&quot; for longer processes if shorter processes
                continuously arrive.</span></p>
        <h1 class="c4" id="h.7lw8qnzb9iz4"><span class="c12 c8" style="font-weight: bold; font-size: 26px;">6.
                Starvation:</span></h1>
        <p class="c0"><span class="c5">While Non-Preemptive SJF is efficient for short processes, longer processes may
                face
                starvation if a continuous stream of short processes arrives.</span></p>
