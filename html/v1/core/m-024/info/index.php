        <p class="c14 title" id="h.pw49p27mxon7"><span class="c10">Page-Buffering Algorithms</span></p>
        <p class="c1 c3"><span class="c7"></span></p>
        <ul class="c9 lst-kix_4e8b3acfsab0-0 start">
            <li class="c0 li-bullet-0"><span class="c7">Keep a pool of free frames, always</span></li>
        </ul>
        <ul class="c9 lst-kix_4e8b3acfsab0-1 start">
            <li class="c1 c5 li-bullet-0"><span class="c7">Then frame available when needed, not found at fault
                    time</span>
            </li>
            <li class="c1 c5 li-bullet-0"><span class="c7">Read page into free frame and select victim to evict and add
                    to
                    free pool</span></li>
            <li class="c1 c5 li-bullet-0"><span class="c7">When convenient, evict victim</span></li>
        </ul>
        <ul class="c9 lst-kix_4e8b3acfsab0-0">
            <li class="c0 li-bullet-0"><span class="c7">Possibly, keep list of modified pages</span></li>
        </ul>
        <ul class="c9 lst-kix_4e8b3acfsab0-1 start">
            <li class="c1 c5 li-bullet-0"><span class="c7">When backing store otherwise idle, write pages there and set
                    to
                    non-dirty</span></li>
        </ul>
        <ul class="c9 lst-kix_4e8b3acfsab0-0">
            <li class="c0 li-bullet-0"><span class="c7">Possibly, keep free frame contents intact and note what is in
                    them</span></li>
        </ul>
        <ul class="c9 lst-kix_4e8b3acfsab0-1 start">
            <li class="c1 c5 li-bullet-0"><span class="c7">If referenced again before reused, no need to load contents
                    again
                    from disk</span></li>
            <li class="c1 c5 li-bullet-0"><span class="c7">Generally useful to reduce penalty if wrong victim frame
                    selected
                    &nbsp;</span></li>
        </ul>
