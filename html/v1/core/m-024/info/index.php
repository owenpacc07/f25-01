        <p class="c22 title" id="h.96drq47lg7n6"><span class="c16 c20"
                style="color: green; text-decoration: underline; font-weight: bold;">A Quick Abstract:</span></p>
        <h1 class="c4" id="h.inldi2niai8t"><span class="style=" font-weight: bold; font-size: 26px;">1. Introduction to
                Page Replacement:</span></h1>
        <p class="c1"><span class="c8">Page replacement algorithms are critical in managing memory efficiently in
                virtual
                memory systems. Both LFU and MFU focus on the frequency of page usage to determine which page to replace
                when a page fault occurs.</span></p>
        <p class="c1 c3"><span class="c8"></span></p>
        <h1 class="c4" id="h.a4p85nwum3n7"><span class="style=" font-weight: bold; font-size: 26px;">2. LFU (Least
                Frequently Used) Page Replacement
                Algorithm:</span></h1>
        <h2 class="c11" id="h.mgtt51fuqrjq"><span class="c6" style="text-decoration: underline;">Working:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Maintain a count of how often each page is referenced.</span></p>
        <h2 class="c11" id="h.ouf2tlhzwtjc"><span class="c6" style="text-decoration: underline;">When a page fault
                occurs:</span></h2>
        <p class="c1"><span class="c8">Replace the page with the lowest usage count.</span></p>
        <h2 class="c11" id="h.xpsx4f2ahfio"><span class="c6" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Effective in capturing both temporal and spatial locality.</span></p>
        <h2 class="c11" id="h.9n9o04vf9aqg"><span class="c6" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Requires additional mechanisms to maintain usage counts, which can be
                resource-intensive.</span></p>
        <h1 class="c4" id="h.eg5w6un6vmtl"><span class="style=" font-weight: bold; font-size: 26px;">3. MFU (Most
                Frequently Used) Page Replacement
                Algorithm:</span>
        </h1>
        <h2 class="c11" id="h.t3h2pvnkz3fl"><span class="c6" style="text-decoration: underline;">Working:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Maintain a count of how often each page is referenced.</span></p>
        <h2 class="c11" id="h.war8kvagphg8"><span class="c6" style="text-decoration: underline;">When a page fault
                occurs:</span></h2>
        <p class="c1"><span class="c8">Replace the page with the highest usage count.</span></p>
        <h2 class="c11" id="h.qoxfzpd6dnxx"><span class="c6" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Simplicity in implementation.</span></p>
        <p class="c1"><span class="c8">Suitable for environments where frequently used pages are likely to continue
                being
                used.</span></p>
        <h2 class="c11" id="h.grciggj5ns74"><span class="c6" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">May struggle in scenarios with changing access patterns.</span></p>
        <h1 class="c4" id="h.7ss02biuttx4"><span class="style=" font-weight: bold; font-size: 26px;">4. Example
                Scenario:</span></h1>
        <p class="c1"><span class="c8">Consider a memory system with three frames (A, B, C) and a sequence of page
                requests:
                1, 2, 3, 4, 1, 2, 5, 1, 2, 3.</span></p>
        <p class="c1 c3"><span class="c8"></span></p>
        <h2 class="c11" id="h.4dykrtgzz6by"><span class="c6" style="text-decoration: underline;">LFU:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Initial state: | - | - | - |</span></p>
        <p class="c1"><span class="c8">Page 1 is requested: | 1 | - | - |</span></p>
        <p class="c1"><span class="c8">Page 2 is requested: | 1 | 2 | - |</span></p>
        <p class="c1"><span class="c8">Page 3 is requested: | 1 | 2 | 3 |</span></p>
        <p class="c1"><span class="c8">Page 4 is requested: | 4 | 2 | 3 | (Page 1 is the least frequently used, so it is
                replaced)</span></p>
        <p class="c1"><span class="c8">... and so on.</span></p>
        <h2 class="c11" id="h.6r6b3j7vre0w"><span class="c6" style="text-decoration: underline;">MFU:</span></h2>
        <p class="c1 c3"><span class="c8"></span></p>
        <p class="c1"><span class="c8">Initial state: | - | - | - |</span></p>
        <p class="c1"><span class="c8">Page 1 is requested: | 1 | - | - |</span></p>
        <p class="c1"><span class="c8">Page 2 is requested: | 1 | 2 | - |</span></p>
        <p class="c1"><span class="c8">Page 3 is requested: | 1 | 2 | 3 |</span></p>
        <p class="c1"><span class="c8">Page 4 is requested: | 1 | 2 | 4 | (Page 3 is the most frequently used, so it is
                replaced)</span></p>
        <p class="c1"><span class="c8">... and so on.</span></p>
        <h1 class="c4" id="h.ukafy2gokl1c"><span class="style=" font-weight: bold; font-size: 26px;">5.
                Conclusion:</span></h1>
        <p class="c1"><span class="c8">LFU and MFU are page replacement algorithms that focus on the frequency of page
                usage. While LFU is effective in capturing both temporal and spatial locality, it requires additional
                mechanisms for maintaining usage counts. MFU, on the other hand, is simpler but may struggle in
                scenarios
                with changing access patterns.</span></p>
