        <p class="c7 title" id="h.96drq47lg7n6"><span class="c16 c18"
                style="color: green; text-decoration: underline; font-weight: bold;">A Quick Abstract:</span></p>
        <h1 class="c12" id="h.ez5felrm8lvh"><span class="c14" style="font-weight: bold; font-size: 26px;">1.
                Introduction to Page Replacement:</span></h1>
        <p class="c9"><span class="c2">In virtual memory systems, page replacement algorithms are essential for managing
                the
                limited physical memory efficiently. When a page fault occurs, and the required page is not in memory,
                the
                operating system must select a page to replace. FIFO is one such page replacement algorithm that follows
                the
                principle of replacing the oldest page in memory.</span></p>
        <p class="c6"><span class="c2"></span></p>
        <h1 class="c12" id="h.kayhiir2p2xr"><span class="c14" style="font-weight: bold; font-size: 26px;">2. FIFO Page
                Replacement Algorithm:</span></h1>
        <h2 class="c17" id="h.5xs7g49sk4un"><span class="c4" style="text-decoration: underline;">Working:</span></h2>
        <p class="c6"><span class="c2"></span></p>
        <p class="c9"><span class="c2">Maintain a queue of pages in memory, tracking the order in which they were
                brought
                into memory.</span></p>
        <h2 class="c17" id="h.mnzlh41nvnl4"><span class="c4" style="text-decoration: underline;">When a page fault
                occurs:</span></h2>
        <p class="c9"><span class="c2">If the page is not in memory, bring it in.</span></p>
        <p class="c9"><span class="c2">If there is no space, remove the page at the front of the queue (oldest
                page).</span>
        </p>
        <p class="c9"><span class="c2">Add the new page to the back of the queue.</span></p>
        <h2 class="c17" id="h.i6htgwi2g47v"><span class="c4" style="text-decoration: underline;">Advantages:</span></h2>
        <p class="c6"><span class="c2"></span></p>
        <p class="c9"><span class="c2">Simple and easy to implement.</span></p>
        <p class="c9"><span class="c2">Provides a fair approximation of least recently used (LRU).</span></p>
        <h2 class="c17" id="h.ju383nilu8aw"><span class="c4" style="text-decoration: underline;">Disadvantages:</span>
        </h2>
        <p class="c6"><span class="c2"></span></p>
        <p class="c9"><span class="c2">May suffer from the Belady&#39;s Anomaly (increasing the number of frames may not
                always decrease page faults).</span></p>
        <h1 class="c12" id="h.el1dffzgetch"><span class="c14" style="font-weight: bold; font-size: 26px;">3. Example
                Scenario:</span></h1>
        <p class="c9"><span class="c2">Consider a memory system with three frames (A, B, C) and a sequence of page
                requests:
                1, 2, 3, 4, 1, 2, 5, 1, 2, 3.</span></p>
        <p class="c6"><span class="c2"></span></p>
        <p class="c9"><span class="c2">Initial state: | - | - | - |</span></p>
        <p class="c9"><span class="c2">Page 1 is requested: | 1 | - | - |</span></p>
        <p class="c9"><span class="c2">Page 2 is requested: | 1 | 2 | - |</span></p>
        <p class="c9"><span class="c2">Page 3 is requested: | 1 | 2 | 3 |</span></p>
        <p class="c9"><span class="c2">Page 4 is requested: | 4 | 2 | 3 | (Page 1 is replaced, as it was the first one
                in)</span></p>
        <p class="c9"><span class="c2">Page 1 is requested: | 4 | 2 | 1 | (Page 3 is replaced)</span></p>
        <p class="c9"><span class="c2">... and so on.</span></p>
        <h1 class="c12" id="h.dxb0n1h11sw4"><span class="c14" style="font-weight: bold; font-size: 26px;">4.
                Conclusion:</span></h1>
        <p class="c9"><span class="c2">FIFO is a straightforward page replacement algorithm based on the principle of
                replacing the oldest page in memory. While simple to implement, it may not always provide optimal
                results in
                terms of minimizing page faults.</span></p>
                
